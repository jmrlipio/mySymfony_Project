<?php

namespace Jone\BlogBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Property
 */
class Property
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $propertyName;

    /**
     * @var string
     */
    private $propertyDetail;

    /**
     * @var string
     */
    private $propertyImage;

    /**
     * @var string
     */
    private $slug;

    /**
     * @var \DateTime
     */
    private $datePublished;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set propertyName
     *
     * @param string $propertyName
     * @return Property
     */
    public function setPropertyName($propertyName)
    {
        $this->propertyName = $propertyName;

        return $this;
    }

    /**
     * Get propertyName
     *
     * @return string 
     */
    public function getPropertyName()
    {
        return $this->propertyName;
    }

    /**
     * Set propertyDetail
     *
     * @param string $propertyDetail
     * @return Property
     */
    public function setPropertyDetail($propertyDetail)
    {
        $this->propertyDetail = $propertyDetail;

        return $this;
    }

    /**
     * Get propertyDetail
     *
     * @return string 
     */
    public function getPropertyDetail()
    {
        return $this->propertyDetail;
    }

    /**
     * Set propertyImage
     *
     * @param string $propertyImage
     * @return Property
     */
    public function setPropertyImage($propertyImage)
    {
        $this->propertyImage = $propertyImage;

        return $this;
    }

    /**
     * Get propertyImage
     *
     * @return string 
     */
    public function getPropertyImage()
    {
        return $this->propertyImage;
    }

    /**
     * Set slug
     *
     * @param string $slug
     * @return Property
     */
    public function setSlug($slug)
    {
        $this->slug = $slug;

        return $this;
    }

    /**
     * Get slug
     *
     * @return string 
     */
    public function getSlug()
    {
        return $this->slug;
    }

    /**
     * Set datePublished
     *
     * @param \DateTime $datePublished
     * @return Property
     */
    public function setDatePublished($datePublished)
    {
        $this->datePublished = $datePublished;

        return $this;
    }

    /**
     * Get datePublished
     *
     * @return \DateTime 
     */
    public function getDatePublished()
    {
        return $this->datePublished;
    }
}
